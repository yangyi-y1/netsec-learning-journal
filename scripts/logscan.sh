    echo "??????? $LOGFILE ???"
    exit 1
fi

echo "========== ???? Top 5 IP =========="
echo "?????$LOGFILE"
echo ""

grep "Failed password" "$LOGFILE" 2>/dev/null | awk '{print $(NF-3)}' | sort | uniq -c | sort -nr | head -5 | while read count ip; do
    echo "$count ?  $ip"
done

